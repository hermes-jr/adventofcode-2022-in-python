directions: dict[str, tuple[int, int]] = {
    'U': (0, -1),
    'D': (0, 1),
    'L': (-1, 0),
    'R': (1, 0)
}

sqrt_2 = sqrt(2)


class Point2D(object):
    def __init__(self, x: int, y: int):
        self.x = x
        self.y = y

    def __repr__(self):
        return "(Point2D[{0}:{1})".format(self.x, self.y)

    def __str__(self):
        return "[{0}:{1}]".format(self.x, self.y)

    def __hash__(self):
        """Overrides the default implementation"""
        return hash(tuple(sorted(self.__dict__.items())))

    def __eq__(self, other):
        """Overrides the default implementation"""
        if isinstance(other, Point2D):
            return self.x == other.x and self.y == other.y
        return False


def calc_distance(a: Point2D, b: Point2D) -> int:
    dx = abs(a.x - b.x)
    dy = abs(a.y - b.y)
    diagonal = min(dx, dy)
    straight = max(dx, dy)
    leftover = straight - diagonal
    return int(diagonal * sqrt_2 + leftover)


def crawl_closer(h: Point2D, t: Point2D) -> Point2D:
    abs_dist = calc_distance(h, t)
    if __debug__:
        print("Distance between {} and {} is {}".format(h, t, abs_dist))
    if abs_dist > 1:
        dx = h.x - t.x
        dy = h.y - t.y
        new_x = t.x + (int(math.copysign(1, dx)) if dx != 0 else 0)
        new_y = t.y + (int(math.copysign(1, dy)) if dy != 0 else 0)
        new_tail = Point2D(new_x, new_y)
        if __debug__:
            print("Crawling to", new_tail)
        return new_tail
    return t


def p1(input_lines: list[str]) -> int:
    head = Point2D(0, 0)
    tail = Point2D(0, 0)
    visited = set()
    visited.add(tail)

    for line in input_lines:
        direction, distance = line.split(' ')
        distance = int(distance)
        head_dx, head_dy = directions[direction]
        if __debug__:
            print("Next step. Head is moving towards [{}:{}]".format(head.x + head_dx * distance,
                                                                     head.y + head_dy * distance))

        for _ in range(0, distance):
            head = Point2D(head.x + head_dx, head.y + head_dy)
            if __debug__:
                print("Tail {} Head {}".format(tail, head))
            tail = crawl_closer(head, tail)
            visited.add(tail)
    return len(visited)


